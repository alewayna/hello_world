# sum function
def sum_function(a, b):
    sum_sum = a + b
    return sum_sum

# substract function
def sub_function(a, b):
    subtract = a - b
    return subtract

# multiply function
def multi_function(a, b):
    multiply = a * b
    return multiply

# divide function
def div_function(a, b):
    division = a / b
    return division
